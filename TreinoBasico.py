
    #Operaçoes Básicas

a = 10
b = 7

c = input("Digite uma operação: ")

if (c == '+'): 
    print(a+b)
elif (c == '-'):
    print(a-b)
elif (c == '*'):
    print(a*b)
elif (c == '/'):
    print(a/b)
elif (c == '**'):
    print(a**b)
else: 
    print('Operacao nao valida')
    




    

